Xwindow::Xwindow(Board &board)
{
    dis = XOpenDisplay(0);

    if (dis == 0)
    {
        std::cout << "[Xwindow] Error creating display" << std::endl;
        exit(99);
    }

    screen = DefaultScreen(dis);
    w = XCreateSimpleWindow(dis, RootWindow(dis, screen), 10, 10, frameWidth, frameHeight, 1, BlackPixel(dis, screen), WhitePixel(dis, screen));
    XSelectInput(dis, w, ExposureMask | KeyPressMask);
    XMapRaised(dis, w);

    Pixmap pix = XCreatePixmap(dis, w, frameWidth, frameHeight, DefaultDepth(dis, screen));
    gc = XCreateGC(dis, pix, 0, (XGCValues *)0);

    XFlush(dis);
    XFlush(dis);

    XColor xcolor;
    Colormap cmap;

    // Got additional colors from https://en.wikipedia.org/wiki/X11_color_names
    char color_vals[TOTAL_COLORS][11] = {"white", "black", "brown", "burlywood", "slate gray", "seashell"};

    cmap = DefaultColormap(dis, screen);
    for (int i = 0; i < TOTAL_COLORS; ++i)
    {
        XParseColor(dis, cmap, color_vals[i], &xcolor);
        XAllocColor(dis, cmap, &xcolor);
        colors[i] = xcolor.pixel;
    }

    XSetForeground(d, gc, colors[Black]);

    // Make window non-resizeable.
    XSizeHints hints;
    hints.flags = (USPosition | PSize | PMinSize | PMaxSize);

    // Set height
    hints.height = frameHeight;
    hints.base_height = frameHeight;
    hints.min_height = frameHeight;
    hints.max_height = frameHeight;

    // Set width
    hints.width = frameWidth;
    hints.base_width = frameWidth;
    hints.min_width = frameWidth;
    hints.max_width = frameWidth;

    // Set hints
    XSetNormalHints(d, w, &hints);

    XSynchronize(d, True);

    usleep(500);

    // Load board into std::pair arrays
    for (int row = 0; row < 8; row++)
    {
        for (int col = 0; col < 8; col++)
        {
            std::shared_ptr<Piece> thePiece = board.getAt(col, row);
            PieceType pType = thePiece->getPieceType();
            ColorType cType = thePiece->getColorType();

            std::pair<PieceType, ColorType> pieceColorPair = std::pair<PieceType, ColorType>(pType, cType);
            newBoard[row][col] = pieceColorPair;

            oldBoard[row][col] = std::pair<PieceType, ColorType>(PieceType::EMPTY_TILE, ColorType::NONE);
        }
    }

    // Draw checkerboard pattern
    fillRectangle(0, 0, frameWidth, frameWidth, (int)ChessColor::WhiteTile);
    for (int row = 0; row < 8; row++)
    {
        int yCoord = row * tileHeight;
        for (int col = 0; col < 8; col++)
        {
            int xCoord = col * tileWidth;

            // Draw black tiles
            if ((col + row) % 2 == 1)
            {
                fillRectangle(xCoord, yCoord, tileWidth, tileHeight, (int)ChessColor::BlackTile);
            }
        }
    }

    // Draw the board to the window
    diffDraw();
}

